CREATE TABLE category (
    category_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    category_name VARCHAR2(255) NOT NULL UNIQUE,
    description VARCHAR2(1000)
);

CREATE TABLE challenge (
    challenge_no NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    question_text VARCHAR2(4000) NOT NULL,
    answer VARCHAR2(1000) NOT NULL,
    points NUMBER DEFAULT 0 NOT NULL,
    category_id NUMBER NOT NULL,
    is_active NUMBER(1) DEFAULT 1 NOT NULL,
    CONSTRAINT ck_challenge_is_active CHECK (is_active IN (0, 1)),
    CONSTRAINT fk_challenge_category
        FOREIGN KEY (category_id)
        REFERENCES category(category_id)
        ON DELETE CASCADE
);

CREATE TABLE challenge_archive (
    archive_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    challenge_no NUMBER NOT NULL,
    question_text VARCHAR2(4000) NOT NULL,
    answer VARCHAR2(1000) NOT NULL,
    points NUMBER NOT NULL,
    category_id NUMBER,
    is_active NUMBER(1) DEFAULT 0 NOT NULL,
    archived_reason VARCHAR2(30) NOT NULL,
    archived_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT ck_challenge_archive_is_active CHECK (is_active IN (0, 1))
);

CREATE TABLE team (
    team_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    team_name VARCHAR2(255) NOT NULL UNIQUE,
    score NUMBER DEFAULT 0 NOT NULL,
    current_challenge_no NUMBER,
    CONSTRAINT fk_team_current_challenge
        FOREIGN KEY (current_challenge_no)
        REFERENCES challenge(challenge_no)
        ON DELETE SET NULL
);

CREATE TABLE student (
    student_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR2(255) NOT NULL,
    email VARCHAR2(255) NOT NULL UNIQUE,
    team_id NUMBER,
    CONSTRAINT fk_student_team
        FOREIGN KEY (team_id)
        REFERENCES team(team_id)
        ON DELETE SET NULL
);

CREATE TABLE hint (
    hint_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    hint_text VARCHAR2(4000) NOT NULL,
    penalty_points NUMBER DEFAULT 0 NOT NULL,
    challenge_no NUMBER NOT NULL,
    CONSTRAINT fk_hint_challenge
        FOREIGN KEY (challenge_no)
        REFERENCES challenge(challenge_no)
        ON DELETE CASCADE
);

CREATE TABLE hint_usage (
    usage_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    team_id NUMBER NOT NULL,
    hint_id NUMBER NOT NULL,
    CONSTRAINT fk_hint_usage_team
        FOREIGN KEY (team_id)
        REFERENCES team(team_id)
        ON DELETE CASCADE,
    CONSTRAINT fk_hint_usage_hint
        FOREIGN KEY (hint_id)
        REFERENCES hint(hint_id)
        ON DELETE CASCADE,
    CONSTRAINT uq_hint_usage UNIQUE (team_id, hint_id)
);

CREATE TABLE submission (
    submission_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    team_id NUMBER,
    student_id NUMBER,
    challenge_no NUMBER,
    submitted_answer VARCHAR2(4000) NOT NULL,
    is_correct NUMBER(1) DEFAULT 0 NOT NULL,
    CONSTRAINT ck_submission_is_correct CHECK (is_correct IN (0, 1)),
    CONSTRAINT fk_submission_team
        FOREIGN KEY (team_id)
        REFERENCES team(team_id)
        ON DELETE SET NULL,
    CONSTRAINT fk_submission_student
        FOREIGN KEY (student_id)
        REFERENCES student(student_id)
        ON DELETE SET NULL,
    CONSTRAINT fk_submission_challenge
        FOREIGN KEY (challenge_no)
        REFERENCES challenge(challenge_no)
        ON DELETE SET NULL
);

CREATE TABLE submission_event (
    event_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    submission_id NUMBER NOT NULL,
    team_id NUMBER NOT NULL,
    is_correct NUMBER(1) NOT NULL,
    created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT fk_submission_event_submission
        FOREIGN KEY (submission_id)
        REFERENCES submission(submission_id)
        ON DELETE CASCADE,
    CONSTRAINT fk_submission_event_team
        FOREIGN KEY (team_id)
        REFERENCES team(team_id)
        ON DELETE CASCADE
);

CREATE TABLE challenge_first_blood (
