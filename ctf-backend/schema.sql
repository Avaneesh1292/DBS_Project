CREATE OR REPLACE FUNCTION fn_progressive_award (
    p_points IN NUMBER,
    p_attempt_no IN NUMBER
) RETURN NUMBER
IS
    v_multiplier NUMBER := 0.15;
BEGIN
    IF p_attempt_no <= 1 THEN
        v_multiplier := 1.0;
    ELSIF p_attempt_no = 2 THEN
        v_multiplier := 0.85;
    ELSIF p_attempt_no = 3 THEN
        v_multiplier := 0.65;
    ELSIF p_attempt_no = 4 THEN
        v_multiplier := 0.50;
    ELSIF p_attempt_no = 5 THEN
        v_multiplier := 0.40;
    END IF;

    RETURN GREATEST(ROUND(NVL(p_points, 0) * v_multiplier), 0);
END;
/

CREATE OR REPLACE PROCEDURE sp_get_leaderboard (
    p_result OUT SYS_REFCURSOR
)
IS
BEGIN
    OPEN p_result FOR
        SELECT team_id, team_name, score
        FROM team
        ORDER BY score DESC, team_name ASC;
END;
/

CREATE OR REPLACE TRIGGER trg_submission_event
AFTER INSERT ON submission
FOR EACH ROW
BEGIN
    INSERT INTO submission_event (submission_id, team_id, is_correct)
    VALUES (:NEW.submission_id, :NEW.team_id, :NEW.is_correct);

    IF :NEW.is_correct = 1 THEN
        BEGIN
            INSERT INTO challenge_first_blood (
                challenge_no,
                team_id,
                submission_id,
                student_id
            ) VALUES (
                :NEW.challenge_no,
                :NEW.team_id,
                :NEW.submission_id,
                :NEW.student_id
            );
        EXCEPTION
            WHEN DUP_VAL_ON_INDEX THEN
                NULL;
        END;
    END IF;
END;
/

CREATE OR REPLACE TRIGGER trg_challenge_archive_on_inactive
BEFORE UPDATE OF is_active ON challenge
FOR EACH ROW
WHEN (OLD.is_active = 1 AND NEW.is_active = 0)
BEGIN
    INSERT INTO challenge_archive (
        challenge_no,
        question_text,
        answer,
        points,
        category_id,
        is_active,
        archived_reason
    ) VALUES (
        :OLD.challenge_no,
        :OLD.question_text,
        :OLD.answer,
        :OLD.points,
        :OLD.category_id,
        0,
        'DEACTIVATED'
    );
END;
/

CREATE OR REPLACE TRIGGER trg_challenge_archive_on_delete
BEFORE DELETE ON challenge
FOR EACH ROW
BEGIN
    INSERT INTO challenge_archive (
        challenge_no,
        question_text,
        answer,
        points,
        category_id,
        is_active,
        archived_reason
    ) VALUES (
        :OLD.challenge_no,
        :OLD.question_text,
        :OLD.answer,
        :OLD.points,
        :OLD.category_id,
        0,
        'DELETED'
    );
END;
/
