-- Idempotent migration for challenge soft-delete + submission retention.
-- Apply this on existing schemas before deploying backend changes.

DECLARE
    v_count NUMBER;
BEGIN
    SELECT COUNT(1)
    INTO v_count
    FROM user_tab_cols
    WHERE table_name = 'CHALLENGE'
      AND column_name = 'IS_ACTIVE';

    IF v_count = 0 THEN
        EXECUTE IMMEDIATE 'ALTER TABLE challenge ADD (is_active NUMBER(1) DEFAULT 1 NOT NULL)';
    END IF;
END;
/

DECLARE
    v_count NUMBER;
BEGIN
    SELECT COUNT(1)
    INTO v_count
    FROM user_constraints
    WHERE constraint_name = 'CK_CHALLENGE_IS_ACTIVE'
      AND table_name = 'CHALLENGE';

    IF v_count = 0 THEN
        EXECUTE IMMEDIATE 'ALTER TABLE challenge ADD CONSTRAINT ck_challenge_is_active CHECK (is_active IN (0, 1))';
    END IF;
END;
