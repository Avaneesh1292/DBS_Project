DECLARE
    v_count NUMBER;
BEGIN
    SELECT COUNT(1)
    INTO v_count
    FROM user_tables
    WHERE table_name = 'CHALLENGE_ARCHIVE';

    IF v_count = 0 THEN
        EXECUTE IMMEDIATE q'[
            CREATE TABLE challenge_archive (
                archive_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                challenge_no NUMBER NOT NULL,
                question_text VARCHAR2(4000) NOT NULL,
                answer VARCHAR2(1000) NOT NULL,
                points NUMBER NOT NULL,
                category_id NUMBER,
                is_active NUMBER(1) DEFAULT 0 NOT NULL,
                archived_reason VARCHAR2(30) NOT NULL,
                archived_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
                CONSTRAINT ck_challenge_archive_is_active CHECK (is_active IN (0, 1))
            )
        ]';
    END IF;
END;
