CREATE OR REPLACE TRIGGER trg_challenge_archive_on_inactive
BEFORE UPDATE OF is_active ON challenge
FOR EACH ROW
WHEN (OLD.is_active = 1 AND NEW.is_active = 0)
BEGIN
    INSERT INTO challenge_archive (
        challenge_no,
        question_text,
        answer,
        points,
        category_id,
        is_active,
        archived_reason
    ) VALUES (
        :OLD.challenge_no,
        :OLD.question_text,
        :OLD.answer,
        :OLD.points,
        :OLD.category_id,
        0,
        'DEACTIVATED'
    );
END;
/

CREATE OR REPLACE TRIGGER trg_challenge_archive_on_delete
BEFORE DELETE ON challenge
FOR EACH ROW
BEGIN
    INSERT INTO challenge_archive (
        challenge_no,
        question_text,
        answer,
        points,
        category_id,
        is_active,
        archived_reason
    ) VALUES (
        :OLD.challenge_no,
        :OLD.question_text,
        :OLD.answer,
        :OLD.points,
        :OLD.category_id,
        0,
        'DELETED'
    );
END;
/
